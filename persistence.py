import json
import os
from typing import Tuple
from models import TreeNode, Node, Leaf

# вспомогательные функции для компактного создания листьев и узлов
def L(animal: str, fact: str) -> Leaf:
    return Leaf(animal, fact)

def N(question: str, yes: TreeNode, no: TreeNode) -> Node:
    return Node(question, yes, no)

def _to_dict(node: TreeNode) -> dict:
    """
    рекурсивно преобразует дерево (treenode) в словарь для json
    возврашает словарь с полями "type" и данными узла
    """
    if isinstance(node, Leaf):
        return {
            "type": "leaf",
            "animal": node.animal,
            "fact": node.fact
        }
    else:
        return {
            "type": "node",
            "question": node.question,
            "yes": _to_dict(node.yes),
            "no": _to_dict(node.no)
        }

def _from_dict(data: dict) -> TreeNode:
    """
    рекурсивно восстанавливает дерево из словаря (обратное преобразование к _to_dict)
    """
    if data["type"] == "leaf":
        return Leaf(data["animal"], data["fact"])  # создаёт и возвращает лист из данных словаря
    else:                                     # иначе тип узла - node
        yes = _from_dict(data["yes"])
        no = _from_dict(data["no"])
        return Node(data["question"], yes, no)  # создаёт и возвращает узел из данных словаря

def create_default_tree() -> TreeNode:
    """
    создаёт начальное дерево вопросов
    возвращает корневой узел node
    """
    # поддерево сельскохозяйственных животных
    сельхоз = N("Это животное даёт молоко?",
                L("Корова", "Коровы могут спать стоя, но видят сны только лёжа."),
                N("Используется для верховой езды?",
                  L("Лошадь", "Лошади могут узнавать себя в зеркале."), # да
                  L("Карликовая свинья", "Карликовые свиньи очень умны и легко приучаются к лотку.")))  # нет
    # поддерево грызунов
    грызуны = N("У него пушистый хвост?",
                L("Шиншилла", "Шиншиллы обладают самым мягким мехом среди всех наземных животных."),
                N("Имеет длинный голый хвост?",
                  L("Декоративная крыса", "Декоративные крысы умеют смеяться, когда их щекочут."),
                  N("Обычно живёт в клетке небольших размеров?",
                    L("Хомяк", "Хомяки могут запасать еды общей массой до 20% собственного веса в своих щеках за раз."),
                    L("Морская свинка", "Морские свинки общаются с помощью более 10 различных звуков."))))
    # поддерево не-грызунов (кошки, собаки, кролики)
    не_грызуны = N("Мяукает?",
                   L("Кошка", "Кошки могут поворачивать уши на 180 градусов."),
                   N("Обычно крупнее кошки?",
                     L("Собака", "Собаки понимают до 250 слов и жестов."),
                     L("Кролик", "Кролики от радости подпрыгивают и делают смешные повороты в воздухе.")))
    # поддерево млекопитающих
    млекопитающие = N("Это сельскохозяйственное животное?",
                      сельхоз,
                      N("Это грызун?",
                        грызуны,
                        не_грызуны))
    # поддерево птиц
    птицы = N("Умеет говорить?",
              L("Попугай", "Некоторые попугаи могут запомнить и использовать до 1000 слов."),
              L("Канарейка", "Канарейки способны различать музыкальные тона и даже подражать мелодиям."))
    # поддерево не-птиц (рыбы, черепахи, игуаны)
    не_птицы = N("Живёт в воде?",
                 L("Золотая рыбка", "Золотые рыбки могут узнавать своих хозяев и запоминать время кормления."),
                 N("У него есть панцирь?",
                   L("Черепаха", "Некоторые черепахи могут дышать задом, используя анальное дыхание."),
                   L("Игуана", "Игуаны могут находиться под водой до 30 минут.")))
    # поддерево не-млекопитающих
    не_млекопитающие = N("Это птица?",
                         птицы,
                         не_птицы)
    # корневой узел - начальный вопрос игры
    root = N("Это млекопитающее?",
             млекопитающие,
             не_млекопитающие)
    return root

def save_game(root: TreeNode, filepath: str):
    """
    сохраняет текущее дерево в json-файл (без статистики)

    """
    data = _to_dict(root)                     # преобразует дерево в словарь
    with open(filepath, 'w', encoding='utf-8') as f:  # открывает файл на запись с кодировкой utf-8
        json.dump(data, f, ensure_ascii=False, indent=2)  # записывает json

def load_game(filepath: str) -> TreeNode:
    """
    загружает дерево из json-файла
    если файл отсутствует или повреждён, возвращает начальное дерево

    """
    if not os.path.exists(filepath):
        print("файл сохранения не найден. создаётся новое дерево.")
        return create_default_tree()          # возвращает начальное дерево
    try:
        with open(filepath, 'r', encoding='utf-8') as f:  # открывает файл на чтение
            data = json.load(f)               # загружает словарь из json
        tree = _from_dict(data)               # восстанавливает дерево из словаря
        return tree
    except (json.JSONDecodeError, KeyError) as e:  # перехватывает ошибки json и отсутствия ключей
        print(f"ошибка загрузки сохранения: {e}. будет создано новое дерево.")
        return create_default_tree()